/**
 * File operation helpers for tests
 */

import { readFileSync, writeFileSync, unlinkSync, existsSync, mkdirSync } from 'fs';
import { join } from 'path';

const TEST_FILES_DIR = join(process.cwd(), 'tests', 'temp-files');

/**
 * Ensure test files directory exists
 */
export function ensureTestFilesDir() {
  if (!existsSync(TEST_FILES_DIR)) {
    mkdirSync(TEST_FILES_DIR, { recursive: true });
  }
}

/**
 * Create a test file
 */
export function createTestFile(filename: string, content: string | Buffer): string {
  ensureTestFilesDir();
  const filePath = join(TEST_FILES_DIR, filename);
  writeFileSync(filePath, content);
  return filePath;
}

