/**
 * Read a test file
 */
export function readTestFile(filePath: string): Buffer {
  return readFileSync(filePath);
}

/**
 * Delete a test file
 */
export function deleteTestFile(filePath: string): void {
  if (existsSync(filePath)) {
    unlinkSync(filePath);
  }
}

/**
 * Clean up all test files
 */
export function cleanupTestFiles(): void {
  if (existsSync(TEST_FILES_DIR)) {
    const { readdirSync, unlinkSync, rmdirSync } = require('fs');
    const files = readdirSync(TEST_FILES_DIR);
    for (const file of files) {
      unlinkSync(join(TEST_FILES_DIR, file));
    }
    rmdirSync(TEST_FILES_DIR);
  }
}

/**
 * Create a mock File object for browser-like tests
 */
export function createMockFile(
  name: string,
  content: string | Buffer,
  type: string = 'text/plain'
): File {
  const blob = new Blob([content], { type });
  return new File([blob], name, { type });
}







